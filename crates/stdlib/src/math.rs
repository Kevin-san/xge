
/// Adds two 32-bit integers
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

/// Subtracts two 32-bit integers
pub fn sub(a: i32, b: i32) -> i32 {
    a - b
}

/// Multiplies two 32-bit integers
pub fn mul(a: i32, b: i32) -> i32 {
    a * b
}

/// Divides two 32-bit integers
pub fn div(a: i32, b: i32) -> i32 {
    a / b
}

/// Adds two 64-bit integers
pub fn add64(a: i64, b: i64) -> i64 {
    a + b
}

/// Subtracts two 64-bit integers
pub fn sub64(a: i64, b: i64) -> i64 {
    a - b
}

/// Multiplies two 64-bit integers
pub fn mul64(a: i64, b: i64) -> i64 {
    a * b
}

/// Divides two 64-bit integers
pub fn div64(a: i64, b: i64) -> i64 {
    a / b
}

